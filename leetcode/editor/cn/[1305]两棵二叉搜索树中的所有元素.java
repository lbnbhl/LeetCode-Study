/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    List<Integer> res = new ArrayList<>();
    List<Integer> list = new ArrayList<>();
    public List<Integer> getAllElements(TreeNode root1, TreeNode root2) {
        dfs(root1);
        int left=list.size();
        dfs(root2);
        int right = list.size();
        int i=0,j=left;
        while (i < left && j < right) {
            if (list.get(i) < list.get(j)){
                res.add(list.get(i++));
            }else {
                res.add(list.get(j++));
            }
        }

        if (i == left){
            for (; j < right; j++) {
                res.add(list.get(j));
            }
        }

        if (j == right){
            for ( ; i < left; i++) {
                res.add(list.get(i));
            }
        }

        return res;
    }

    void dfs(TreeNode root){
        if (root == null)
            return;
        dfs(root.left);
        list.add(root.val);
        dfs(root.right);
    }
}
