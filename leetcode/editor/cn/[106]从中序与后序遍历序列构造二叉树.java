/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    int root;
    public TreeNode buildTree(int[] inorder, int[] postorder) {
        root = postorder.length -1;
        return traversal(inorder,postorder,0, inorder.length-1);
    }

    TreeNode traversal(int[] inorder, int[] postorder,int left,int right){
        if (left > right)
            return null;
        int i,num = postorder[root--];
        TreeNode res = new TreeNode(num);
        for (i = left; i < right; i++) {
            if (inorder[i] == num)
                break;
        }
        res.right = traversal(inorder,postorder,i+1,right);
        res.left = traversal(inorder,postorder,left,i-1);
        return res;
    }
}
